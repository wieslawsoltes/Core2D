using System;

namespace Test2d
{
    /// <summary>
    /// Specifies line endings arrow types.
    /// </summary>
    public enum ArrowType
    {
        /// <summary>
        /// No arrow is drawn.
        /// </summary>
        None,
        /// <summary>
        /// Draw rectangle shape as arrow.
        /// </summary>
        Rectangle,
        /// <summary>
        /// Draw ellipse shapes as arrow
        /// </summary>
        Ellipse,
        /// <summary>
        /// Draw tow line shapes as arrow.
        /// </summary>
        Arrow
    }
}
